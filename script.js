btnGuess.addEventListener('click', function()
{
 let convertInput;
 convertInput =  txtInput.value.charAt(0).toUpperCase() + txtInput.value.slice(1);
  
  if(convertInput.trim() == '')
  {
    gameMsg.textContent = 'Please write something! Pika!';
    pokeCry(convertInput.trim());
    txtInput.value = '';
  }
  else if(convertInput == KantoPokemon[randomIndex].name)
  {
    let correctSound = new Audio('111-pokemon recovery.mp3');
    txtInput.value = '';
    playerScore++;
    playerScoreDisplay.textContent = playerScore;
    gameMsg.textContent = `Correct answer! It's ${KantoPokemon[randomIndex].name}!`;
    pokemonIMG.classList.add('imgReveal');
    displayName.textContent = `${KantoPokemon[randomIndex].name}`;
    displayDexNum.textContent = `Dex Number: ${KantoPokemon[randomIndex].dexNo}`;
    displayType1.textContent = `${KantoPokemon[randomIndex].type1}`;
    displayType2.textContent = `${KantoPokemon[randomIndex].type2}`;

    displayType1.classList.toggle(KantoPokemon[randomIndex].type1);
    displayType2.classList.toggle(KantoPokemon[randomIndex].type2);

    correctSound.play();
    
    if(playerScore > bestScore)
    {
      newBestScore = playerScoreDisplay.textContent;
    }

    if(playerScore == 10)
    {
      alert('You win!!! click Ok to continue playing for best score.');
      pokeCry(playerScore);
    }
    newBestScore = playerScore;

    setTimeout(nextPokemonCorrect, 5000);
    //Push the revealed pokemon to the array
    revealedPokemonArray.push(KantoPokemon[randomIndex]);
  }
  else
  {
    txtInput.value = '';
    newBestScore = playerScore;
    if(newBestScore > bestScore && playerScore > 10)
    {
      bestScore = newBestScore;
      bestScoreDisplay.textContent = bestScore;
      newBestScore = 0;
      playerScore = 0;
      playerScoreDisplay.textContent = playerScore;
      pokeCry(bestScore);
      gameMsg.textContent = `Great Job! new best score!`;
    }
    else if(newBestScore < bestScore)
    {
      bestScore = bestScoreDisplay.textContent;
      playerScore = 0;
      newBestScore = 0;
      playerScoreDisplay.textContent = playerScore;
      pokeCry();
      gameMsg.textContent = `Wrong answer!, the wild Poke'mon escaped!`;
    }
    else
    {
      newBestScore = playerScore;
      bestScore = newBestScore;
      playerScore = 0;
      newBestScore = 0;
      playerScoreDisplay.textContent = playerScore;
      bestScoreDisplay.textContent = bestScore;
      pokeCry();
      gameMsg.textContent = `Wrong answer!, the wild Poke'mon escaped!`;
    }
    setTimeout(nextPokemonWrong, 5000);
  }

});


//next pokemon reveal function
function nextPokemonCorrect()
{
  displayType1.classList.remove(
    'Normal','Fire','Fighting','Water','Flying',
    'Grass','Poison','Electric','Ground','Psychic',
    'Rock','Ice','Bug','Dragon','Ghost',
    'Dark','Steel','Fairy', 'NA');
  displayType2.classList.remove(
    'Normal','Fire','Fighting','Water','Flying',
    'Grass','Poison','Electric','Ground','Psychic',
    'Rock','Ice','Bug','Dragon','Ghost',
    'Dark','Steel','Fairy', 'NA');
  
  // filter from the original array values that are not revealed from the revealedArray
  KantoPokemon = KantoPokemon.filter(function(pokemons){
    return !revealedPokemonArray.includes(pokemons);
  });
  
  randomIndex = Math.floor(Math.random() * (KantoPokemon.length));

  if(KantoPokemon[randomIndex] == undefined && txtInput.value.trim() == '')
  {
    alert('Congrats guessing all 151 pokemon! Thank you for playing! refresh the browser to play again');
    pokeCry(151);
    txtInput.disabled = true;
    btnGuess.disabled = true;
    btnSound.disabled = true;
    gameMsg.textContent = 'Congrats Guessing 151! please refresh to play again';
  }
  else
  {
    pokemonIMG.src = KantoPokemon[randomIndex].imgSrc;
    gameMsg.textContent = "A wild Poke'mon appeared!!!";
  }

  pokemonIMG.classList.remove('imgReveal');
  displayName.textContent = '???';
  displayDexNum.textContent = 'Dex Number: 000';
  displayType1.textContent = 'Type 1: ----';
  displayType2.textContent = 'Type 2: ----';
}

