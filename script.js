//hint sound button
btnSound.addEventListener('click', pokeCry);

function pokeCry(input)
{
  if(input == '')
  {
    //angry pikachu sound
    let pokeSound = new Audio(encodeURI('pikachu-sounds/025 - Pikachu (07).wav'));
    pokeSound.play();
  }
  else if(input == 10)
  {
    //happy pikachu sound
    let pokeSound = new Audio(encodeURI('pikachu-sounds/025 - Pikachu (38).wav'));
    pokeSound.play();
  }
  else if(input == 151)
  {
    let pokeSound = new Audio(encodeURI('pikachu-sounds/025 - Pikachu (35).wav'));
    pokeSound.play();
  }
  else if(input > 10)
  {
    //another pikachu sound new high score
    let pokeSound = new Audio(encodeURI('pikachu-sounds/025 - Pikachu (96).wav'));
    pokeSound.play();
  }
  else
  {
    // on sound hint
    let pokeSound = new Audio(encodeURI(KantoPokemon[randomIndex].cry));
    pokeSound.play();
  }
  
}




